
public class TreinandoFor {
	public static void main(String[] args) {
		
		
		//laço do tipo for para imprimir todos os múltiplos de 3, entre 1 e 100.
		
		/*
		 * for (int i = 1; i < 100; i++) 
		 * if (i % 3 == 0) { 
		 * System.out.println(i); 
		 * }
		 */
		
		
		
		//Forma mais simples de se fazer 
		for (int i = 3; i < 100; i += 3) {
			System.out.println(i);
		}
	}
}
